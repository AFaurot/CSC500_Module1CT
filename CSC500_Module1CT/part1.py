def main():
    # Gather user input
    num1 = int(input("Enter a whole number: \n"))
    num2 = int(input("Enter another whole number: \n"))
    # Print sum and difference
    print(num1, '+', num2, 'equals', num1 + num2)
    print(num1, '-', num2, 'equals', num1 - num2)


if __name__ =='__main__': main()